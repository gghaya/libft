#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	n ;
	int			i ;
	int			sign ;

	i = 0 ;
	n = 0 ;
	sign = 1 ;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
			n = n * 10 + str[i] - 48 ;
			i++;
	}
	return (n * sign);
}
